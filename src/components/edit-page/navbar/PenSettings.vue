<template>
  <div class="nav-group">
    <button @click="disableEraser" id="pen" class="nav-btn btn-fab-mini tools"
            :class="{ penDisabled: !eraserDisabled, penEnabled: eraserDisabled }">
      <img src="../../../../static/img/pen.svg">
    </button>
    <button @click="enableEraser" id="eraser" class="nav-btn btn-fab-mini tools"
            :class="{ eraserEnabled: !eraserDisabled, eraserDisabled: eraserDisabled }">
      <img src="../../../../static/img/eraser.svg">
    </button>
    <div @click="showThicknessPanel" class="pensettings thickness" ref="thickness">
      <div class="thickness-icon" :style="activeThickness.itemStyle"></div>
      <span>{{activeThickness.value}}</span>
    </div>
    <thickness-panel id="thickness-panel" v-show="displayThicknessPanel" :style="{ left: thicknessPanelLeft }"/>
    <div class="colors">
      <template v-for="color in colors">
        <pen-color :color="color" :checked="color === '#000000'" :key="color"/>
      </template>
      <color-picker/>
    </div>
  </div>
</template>

<script>
import EventBus from '../../../event-bus';
import PenColor from './PenColor';
import ColorPicker from './ColorPicker';
import ThicknessPanel from './ThicknessPanel';

export default {
  name: 'pen-setting',
  data() {
    return {
      colors: ['#000000', '#808080', '#D9D9D9', '#1A8CFF', '#FF1A40', '#2BD965', '#FFDD33'],
      eraserDisabled: true,
      displayThicknessPanel: false,
      thicknessPanelLeft: '',
      activeThickness: {
        value: 1,
        itemStyle: 'width: 5px; height: 5px; margin-top: 6px',
      },
    };
  },
  components: {
    PenColor,
    ColorPicker,
    ThicknessPanel,
  },
  mounted() {
    EventBus.$on('hideThicknessPanel', () => {
      this.displayThicknessPanel = false;
    });

    EventBus.$on('pointerDown', () => {
      this.displayThicknessPanel = false;
    });

    EventBus.$on('thicknessUpdated', (data) => {
      this.activeThickness = data;
    });

    EventBus.$on('colorChanged', () => {
      this.disableEraser();
    });

    EventBus.$on('customColorChanged', () => {
      this.disableEraser();
    });
  },
  methods: {
    showThicknessPanel() {
      this.displayThicknessPanel = true;
      this.thicknessPanelLeft = `${this.$refs.thickness.offsetLeft}px`;
    },
    enableEraser() {
      document.body.style.cursor = 'url(\'static/img/eraser-cursor.cur\'), auto';
      this.eraserDisabled = false;
      EventBus.$emit('eraserEnabled');
    },
    disableEraser() {
      document.body.style.cursor = 'initial';
      this.eraserDisabled = true;
      EventBus.$emit('eraserDisabled');
    },
  },
};
</script>

<style scoped>

  .eraserEnabled {
    border-bottom: #1A9FFF 2px solid;
  }

  .penDisabled {
    border-bottom: initial;
    padding-top: 0;
  }

  .eraserDisabled {
    border-bottom: initial;
  }

  .penEnabled {
    border-bottom: #1A9FFF 2px solid;
    padding-top: 2px;
  }

</style>
